package com.myspring.tt.myboot;

public class Son {
	private String name;
	private int age;
	
	public Son(String name,int age){
		name = name;
		age = age;
	}
	public Son(){
		
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	
}
